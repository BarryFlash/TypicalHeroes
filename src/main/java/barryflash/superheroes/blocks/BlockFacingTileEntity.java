public abstract class BlockFacingTileEntity<TE extends TileEntity> extends BlockFacingBase {

	public BlockFacingTileEntity(String name, Material material, CreativeTabs tab, int hardness) {
		super(name, material, tab, hardness);
	}
	
	public abstract Class<TE> getTileEntityClass();
	
	public TE getTileEntity(IBlockAccess world, BlockPos pos) {
		return (TE)world.getTileEntity(pos);
	}
	
	@Override
	public boolean hasTileEntity(IBlockState state) {
		return true;
	}
	
	@Nullable
	@Override
	public abstract TE createTileEntity(World world, IBlockState state);

}